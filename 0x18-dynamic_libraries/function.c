#include "functions.h"
/**
* add - function to add 2 numbers a and b
* @a: integer number
* @b: inteber number
* Return: a + b
*/
int add(int a, int b)
{
	return (a + b);
}
/**
* sub - function to subtract 2 numbers a and b
* @a: integer number
* @b: inteber number
* Return: a + b
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - function to multiply 2 numbers a and b
* @a: integer number
* @b: inteber number
* Return: a + b
*/
int mul(int a, int b)
{
	return (a * b);
}

/**
* div - function to divide 2 numbers a and b
* @a: integer number
* @b: inteber number
* Return: a + b
*/
double div(int a, int b)
{
	return (a / b);
}

/**
* mod - function to mod 2 numbers a and b
* @a: integer number
* @b: inteber number
* Return: a + b
*/
int mod(int a, int b)
{
	return (a % b);
}
