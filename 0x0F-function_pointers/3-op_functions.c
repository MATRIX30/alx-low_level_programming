/**
* op_add - function to add 2 numbers
* @a: first int number to add
* @b: second number
* Return: addition of a and b
*/
int op_add(int a, int b)
{
	return (a + b);
}


/**
* op_sub - function to subtract 2 numbers
* @a: first int number to add
* @b: second number
* Return: subtraction of a and b
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - function to add 2 numbers
* @a: first int number to add
* @b: second number
* Return: multiplication/ product of a and b
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - function to add 2 numbers
* @a: first int number to add
* @b: second number
* Return: division of a and b
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - function to modulus 2 numbers
* @a: first int number to add
* @b: second number
* Return: modulus of a and b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
